    def __init__(self):
        self.color = Forbiddencity
        self.color_pd=pd.DataFrame(self.color).T
    
    def get_cmap(self,color_range,return_cmap=True):
        return Forbidden_Cmap(color_range,return_cmap=return_cmap)
    

    def get_color(self,name):
        return self.color_pd.loc[self.color_pd['name']==name]
    
    def get_color_rgb(self,name):
        return self.color_pd.loc[self.color_pd['name']==name]['color_rgb'].values[0]
    
    def get_cmap_seg(self,colors,N=256):
        from matplotlib.colors import LinearSegmentedColormap
        # Define the colors for the gradient
        colors = np.array(colors) / 255.0
        # Create a colormap from the list of colors
        cmap_name = 'custom_cmap'
        cmap = LinearSegmentedColormap.from_list(cmap_name, colors, N=N)
        return cmap

    # Function to create HTML table with colors
    def visual_color(self, loc_range=(0,385),num_per_row=10):
        df=self.color_pd.iloc[loc_range[0]:loc_range[1]]
        html = '<table style="border-collapse: collapse; width: 100%;">'
        for i in range(0, len(df), num_per_row):
            # Add a row for color names
            html += '<tr>'
            for j in range(i, min(i + num_per_row, len(df))):
                html += f'<td style="border: 1px solid black; padding: 5px;">{df.iloc[j]["name"]}</td>'
            html += '</tr>'
            
            # Add a row for color blocks
            html += '<tr>'
            for j in range(i, min(i + num_per_row, len(df))):
                color = df.iloc[j]["color_html"]
                html += f'<td style="border: 1px solid black; padding: 5px; background-color: {color};"></td>'
            html += '</tr>'
        html += '</table>'
        return html