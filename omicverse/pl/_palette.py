def colormaps_palette(map_name:str)->list:
    r"""Returns a colormap palette.

    colormap's name could be found in `https://pratiman-91.github.io/colormaps/`
    
    Args:
        map_name: Name of the colormap
        
    Returns:
        List of hex color codes for the colormap
    """
    import colormaps
    import matplotlib.colors as mcolors
    cmap = getattr(colormaps, map_name)
    return [mcolors.to_hex(i) for i in cmap.colors]

def optim_palette(adata,basis,colors,palette=None,**kwargs):
    """
    Optimized palette for plotting

    Args:
        adata: AnnData object
        basis: basis for plotting, which is the key of adata.obsm
        colors: key of adata.obs for color
        palette: palette for plotting
        kwargs: kwargs for spaco.colorize

    Returns:
        palette_spaco: palette for plotting
    
    """

    try:
        import spaco
        #print('mofax have been install version:',mfx.__version__)
    except ImportError:
        raise ImportError(
            'Please install the spaco: `pip install spaco-release`.'
        )
    
    adata.obs[colors]=adata.obs[colors].astype('category')

    if (adata.uns[f'{colors}_colors'] is None) and (palette is None):
        if len(adata.obs[colors].cat.categories)>28:
            palette_t=sc.pl.palettes.default_102[:len(list(set(adata.obs[colors].tolist())))]
        else:
            palette_t=sc.pl.palettes.zeileis_28[:len(list(set(adata.obs[colors].tolist())))]
        #palette_t=ditto_color[:len(list(set(adata.obs[colors].tolist())))]
    elif palette!= None:
        palette_t=palette[:len(list(set(adata.obs[colors].tolist())))]
    else:
        palette_t=adata.uns[f'{colors}_colors']
        
    import spaco
    color_mapping = spaco.colorize(
        cell_coordinates=adata.obsm[basis],
        cell_labels=adata.obs[colors],
        colorblind_type='none',
        palette=palette_t,
        **kwargs
    )
    # Order colors by categories in adata
    color_mapping = {k: color_mapping[k] for k in adata.obs[colors].cat.categories}
    palette_spaco = list(color_mapping.values())
    return palette_spaco

def Forbidden_Cmap(color_range,return_cmap=False):
    r"""
    Create a colormap or color list from the Forbidden City color palette.
    
    Args:
        color_range: list or sequence
            Range of color indices to select from the Forbidden City palette
        return_cmap: bool, optional (default=False)
            Whether to return a matplotlib colormap object or HTML color codes
    
    Returns:
        If return_cmap is True, returns matplotlib.colors.ListedColormap
        If return_cmap is False, returns list of HTML color codes
    """
    from matplotlib.colors import ListedColormap
    import numpy as np
    colors = [Forbiddencity[str(color)]['color_rgb'] for color in color_range]
    # Create a ListedColormap
    if return_cmap:
        cmap = ListedColormap(np.array(colors) / 255.0)
        return cmap
    else:
        return [Forbiddencity[str(color)]['color_html'] for color in color_range]
    

